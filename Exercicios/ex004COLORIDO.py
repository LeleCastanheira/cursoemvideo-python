frase = input('Digite algo: ')
print('{}O tipo primitivo desse valor é {}{}'.format('\033[4;44m', type(frase), '\033[m'))
print('\033[7;4;44m''Só tem espaços?', frase.isspace(), '\033[m')
print('\033[4;44m''É um número?', frase.isnumeric(), '\033[m')
print('\033[7;4;44m''É alfabético?', frase.isalpha(), '\033[m')
print('\033[4;44m''É alfanumérico?', frase.isalnum(), '\033[m')
print('\033[7;4;44m''Está em maiúsculo?', frase.isupper(), '\033[m')
print('\033[4;44m''Está em minúscula?', frase.islower(), '\033[m')
print('\033[7;4;44m''Está capitalizada?', frase.istitle(), '\033[m')
